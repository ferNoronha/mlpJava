package redesneurais.classes;

/**
 *
 * @author Fernando A. Noronha
 */
public class Olocotio {
    
    private String n;
    private String classe;

    public Olocotio(String n, String classe) {
        this.n = n;
        this.classe = classe;
    }

    
    
    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }

    public String getClasse() {
        return classe;
    }

    public void setClasse(String classe) {
        this.classe = classe;
    }

    
    
    
}
